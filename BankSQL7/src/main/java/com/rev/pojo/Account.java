package com.rev.pojo;

public class Account 
{
	private int a_id;
	private int u_id;
	private int balance;
	public Account()
	{
		
	}
	public Account(int a_id, int u_id, int balance) {
		super();
		this.a_id = a_id;
		this.u_id = u_id;
		this.balance = balance;
	}
	/**
	 * @return the a_id
	 */
	public int getA_id() {
		return a_id;
	}
	/**
	 * @param a_id the a_id to set
	 */
	public void setA_id(int a_id) {
		this.a_id = a_id;
	}
	/**
	 * @return the u_id
	 */
	public int getU_id() {
		return u_id;
	}
	/**
	 * @param u_id the u_id to set
	 */
	public void setU_id(int u_id) {
		this.u_id = u_id;
	}
	/**
	 * @return the balance
	 */
	public int getBalance() {
		return balance;
	}
	/**
	 * @param balance the balance to set
	 */
	public void setBalance(int balance) {
		this.balance = balance;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "Account [a_id=" + a_id + ", u_id=" + u_id + ", balance=" + balance + "]";
	}
}
